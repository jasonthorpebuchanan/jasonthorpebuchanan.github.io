				<p align="justify" class="style50"><span class="style48">
				Jason Thorpe Buchanan</span><span class="style49"> (b. 1986) is an American composer of contemporary concert music.
				&nbsp;His work draws from a broad variety of aesthetic genres and influences, and has been described by leading composers in the United States and Europe as "sharply-edged", "shimmering," "attractive," "symphonic," and "ambitious."
				&nbsp; He is the founder of <a href="http://www.melosmusic.com" target="_blank">Melos Music</a>, a composer's consortium for which he served as director from 2007-2012, as well as their annual New Music Concert series.
				&nbsp; Jason spent 2010-2011 living in Hamburg, Germany, where he was awarded a Fulbright Fellowship at the Hochschule f&#252;r Musik und Theater as a visiting scholar for studies with Peter Michael Hamel and Manfred Stahnke, as well as courses with Georg Hajdu, and Sascha Lino Lemke while conducting research and interviews in regard to compositional process and aesthetics.
	<br>
	&nbsp;&nbsp; In 2012, his <em>First Study for Alto Saxophone: doublethink</em>, commissioned by Michael Rene Torres, was premiered at the North American Saxophone Alliance biennial conference in Tempe, AZ, receiving the Belle S. Gitelman award from the Eastman School of Music.
	&nbsp; The same year a collaboration with MacArthur "Genius" Award winning visual artist <a href="http://www.annaschuleit.com" target="_blank">Anna Schuleit</a>, <em>First Study for Piano & Electronics: absence</em>, was premiered by <a href="http://danielpesca.com/" target="_blank">Daniel Pesca</a> for the Benson Creative Forum at Eastman.
        &nbsp; <em>Asymptotic Flux: First Study in Entropy</em> for amplified bass clarinet, violin, viola & cello was co-commissioned by the <a href="http://www.switchensemble.com" target="_blank">[Switch~ Ensemble]</a>/<a href="http://www.ossianewmusic.org" target="_blank">Ossia</a> in Rochester and <a href="http://www.ensemble39.com" target="_blank">ensemble39</a> in Philadelphia.
	&nbsp; In 2011 his <em><a href="http://www.melosmusic.com/jason/w_fourberlinsongschamber.php" target="_blank">Berlin Songs</a></em>, for two singers and mixed chamber ensemble, were premiered for a capacity audience at the Akademie der K&#252;nste during the Pan-European Fulbright conference in Berlin, Germany under the baton of Thomas Heuser, who commissioned the work alongside the German/American Fulbright Kommission.
	&nbsp; An expanded orchestration was later recorded and premiered in the U.S. at the 2nd Annual Melos New Music Concert in San Francisco, conducted by the composer.
		     &nbsp; This recording later became a finalist in both the "American Prize" orchestral composition competition (2012) and the ASCAP Morton Gould composition competition (2012).
		     <br>
		      &nbsp;&nbsp; In 2010 three chapters of his orchestral work - <em><a href="http://www.melosmusic.com/jason/w_peganaorchestraone.php" target="_blank">The Gods of Pegãna</a></em>, were read by the Brevard Music Center Orchestra under the baton of Ken Lam.
		     &nbsp; The work was later awarded the distinction of finalist in the ASCAP Morton Gould composition competition (2011), and Honorable Mention in both the ACF Minnesota Composer's Institute competition (2011) and the ACO Underwood/San Diego readings competition (2012).
		     &nbsp; Three excerpts from this work in chamber orchestra reduction were later given their premiere public performance with the Sound ExChange Orchestra in collaboration with the University of Rochester Program of Dance and Movement, conducted by the composer.
		    &nbsp; In 2009, <em>A Zarzuela & Other Lost Works</em> was premiered by the Tad Wind Symphony in Tokyo for an audience of over 800, and recorded with a CD released on the Windstream label in Japan.
		      <br>
				&nbsp; &nbsp; Jason began his studies at age fourteen at the College of San Mateo, CA, later attending San Jos&eacute; State University and the University of Nevada, Las Vegas as a Master's student, where he taught courses in composition and music theory (2008-2010), receiving the highest honors from both institutions as outstanding graduating senior/graduate student.
				&nbsp; He has studied composition with Allan Schindler, Virko Baley, Peter Michael Hamel, Jorge Grossmann, Ricardo Zohn-Muldoon, Pablo Furman, Kevin Puts, Robert Aldridge, and Manfred Stahnke, as well as additional studies with Takayoshi Suzuki and Brad Lubman in conducting, and at Darmstadt with <a href="http://www.aperghis.com" target="_blank">Georges Aperghis</a>, <a href="http://www.edition-peters.com/composer/Ferneyhough-Brian" target="_blank">Brian Ferneyhough</a>, and <a href="http://brahms.ircam.fr/raphael-cendo" target="_blank">Raphaël Cendo</a>.
		      <br>
		      &nbsp;&nbsp; He has received a variety of prestigious academic and artistic awards from ASCAP, ACF, MPE, the NEON and Brevard Music Festivals, UNLV, SJSU, the Eastman School of Music, the American Prize, and the Miami Beach International Animated Film Festival. Recordings of his music are commercially available on the <a href="http://www.melosmusic.com" target="_blank">Melos Music</a> label in the United States and the Windstream label in Japan. 
<br>
          &nbsp;&nbsp;As a conductor and advocate for contemporary music, Jason is committed to providing opportunities for composers as a concert curator, where he has premiered and recorded works by living composers such as <a href="http://www.danieltemkincomposer.com/" target="_blank">Daniel Temkin</a>, <a href="http://www.toniako.com/" target="_blank">Tonia Ko</a>, <a href="http://christopherchandlermusic.com/" target="_blank">Christopher Chandler</a>, <a href="http://www.chintingchan.com/" target="_blank">Chin Ting Chan</a>, Tom Brennan, Weijun Chen, Theocharis Papatrechas, Nathan Stang, and Jennifer Bellor, in addition to his own, with ensembles such as ITCH, the UNLV Brass Ensemble, <a href="http://www.nonsemble6.com/" target="_blank">Nonsemble 6</a>, Ossia, the Eastman Graduate Composers' Sinfonietta, ensemble39, and the Sound ExChange Orchestra.
		     
		      &nbsp; During the 2012-2013 academic year, Jason will serve as assistant conductor for Eastman's premiere New Music Ensemble: <a href="http://www.esm.rochester.edu/ensembles/musicanova/" target="_blank">Musica Nova</a>, with conductor <a href="http://bradlubman.me/" target="_blank">Brad Lubman</a>, as well as a board member of <a href="http://www.ossianewmusic.org" target="_blank">Ossia</a>, coordinator for the Graduate Composers' Sinfonietta, and co-founder/conductor of Eastman's brand new Electroacoustic Initiative, <a href="http://www.switchensemble.com" target="_blank">the [Switch~ Ensemble]</a>.
		      <br>
		      &nbsp; A few of his upcoming compositional engagements include commissions from <a href="http://www.jeffnelsen.com" target="_blank">Jeff Nelsen</a> and <a href="http://www.mikewalkermusic.com/" target="_blank">Michael Walker</a> for a Concerto for two Horns, a work for 14 musicians and live electronics for the [Switch~ Ensemble], and a quartet for <a href="http://www.iktuspercussion.com/" target="_blank">Iktus Percussion</a> in NYC.
		   
		      &nbsp; Jason currently studies composition with Robert Morris and holds a Graduate Teaching Assistantship at the Computer Music Center as a Ph.D. student at the Eastman School of Music in Rochester, NY.</p>
